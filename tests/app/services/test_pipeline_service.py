@pytest.mark.asyncio
async def test_create_and_dispatch_db_failure_initial_save(mock_db_session: MagicMock, mock_uploaded_file_log: UploadedFileLog):
    """Test DB failure during the initial save of PipelineRun."""
    mock_db_session.get.return_value = mock_uploaded_file_log
    mock_db_session.commit.side_effect = [Exception("DB error on first commit"), None] # Fail first commit, succeed second (if any)
    # We expect it to not even reach the point of dispatching the Celery task.

    with pytest.raises(Exception) as exc_info, \
         patch("app.services.pipeline_service.summarize_pdf_task.delay") as mock_delay:
        # The service function re-raises the original DB exception if not caught by specific logic
        # In this case, the first commit failing isn't specifically handled to update status to FAILED,
        # so it should propagate the raw DB error.
        await create_and_dispatch_summary_pipeline(1, mock_db_session)

    assert str(exc_info.value) == "DB error on first commit"
    mock_db_session.get.assert_called_once_with(UploadedFileLog, 1)
    mock_db_session.add.assert_called_once() # Called once for the initial PipelineRun
    mock_db_session.commit.assert_called_once() # Called once, and it failed
    mock_db_session.refresh.assert_not_called() # Should not be called if commit fails
    mock_delay.assert_not_called()

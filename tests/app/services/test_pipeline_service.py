@pytest.mark.asyncio
async def test_create_and_dispatch_celery_dispatch_failure(
    mock_db_session: MagicMock, 
    mock_uploaded_file_log: UploadedFileLog
):
    """Test pipeline creation when Celery task dispatch fails."""
    mock_db_session.get.return_value = mock_uploaded_file_log

    captured_pipeline_run = None
    def refresh_side_effect(obj):
        nonlocal captured_pipeline_run
        if isinstance(obj, PipelineRun):
            obj.id = 1 # Simulate DB assigning an ID
            # Do not assign celery_task_id here as dispatch will fail
            captured_pipeline_run = obj
    mock_db_session.refresh.side_effect = refresh_side_effect

    def add_side_effect(obj):
        nonlocal captured_pipeline_run
        if isinstance(obj, PipelineRun):
             captured_pipeline_run = obj
    mock_db_session.add.side_effect = add_side_effect

    with patch("app.services.pipeline_service.summarize_pdf_task.delay", side_effect=Exception("Celery down")) as mock_delay, \
         pytest.raises(HTTPException) as exc_info:
        await create_and_dispatch_summary_pipeline(1, mock_db_session)

    assert exc_info.value.status_code == 500
    assert "Failed to dispatch summarization task: Celery down" in exc_info.value.detail

    mock_delay.assert_called_once()
    # add/commit/refresh for initial PipelineRun + add/commit/refresh for FAILED status update
    assert mock_db_session.add.call_count == 2
    assert mock_db_session.commit.call_count == 2
    assert mock_db_session.refresh.call_count == 2 # Both refreshes happen

    assert captured_pipeline_run is not None
    assert captured_pipeline_run.status == PipelineRunStatus.FAILED
    assert "Failed to dispatch Celery task: Celery down" in captured_pipeline_run.error_message



@pytest.mark.asyncio
async def test_create_and_dispatch_summary_pipeline_success(
    mock_db_session: MagicMock, 
    mock_uploaded_file_log: UploadedFileLog
):
    """Test successful creation and dispatch of a summary pipeline."""
    mock_db_session.get.return_value = mock_uploaded_file_log
    
    mock_celery_task_result = MagicMock()
    mock_celery_task_result.id = str(uuid.uuid4())

    # Capture the PipelineRun instance passed to db.add and db.refresh
    # and simulate the refresh by adding an id and celery_task_id to it.
    captured_pipeline_run = None

    def refresh_side_effect(obj):
        nonlocal captured_pipeline_run
        if isinstance(obj, PipelineRun):
            obj.id = 1 # Simulate DB assigning an ID
            obj.celery_task_id = mock_celery_task_result.id # Simulate celery task ID assignment
            captured_pipeline_run = obj

    mock_db_session.refresh.side_effect = refresh_side_effect

    # Also ensure that when db.add is called, subsequent db.refresh uses the same object with generated run_uuid
    def add_side_effect(obj):
        nonlocal captured_pipeline_run
        if isinstance(obj, PipelineRun):
            # The run_uuid is generated by default_factory in PipelineRunCreate/PipelineRun
            # So, we don't need to manually set it here if the model works correctly.
            # We will capture it to verify.
            captured_pipeline_run = obj 

    mock_db_session.add.side_effect = add_side_effect

    with patch("app.services.pipeline_service.summarize_pdf_task.delay", return_value=mock_celery_task_result) as mock_delay:
        response = await create_and_dispatch_summary_pipeline(1, mock_db_session)

    mock_db_session.get.assert_called_once_with(UploadedFileLog, 1)
    
    # add is called twice: once for initial PipelineRun, once for updating with celery_task_id
    assert mock_db_session.add.call_count == 2 
    # commit is called twice
    assert mock_db_session.commit.call_count == 2
    # refresh is called twice
    assert mock_db_session.refresh.call_count == 2

    # Check the captured PipelineRun from the refresh_side_effect or add_side_effect
    assert captured_pipeline_run is not None
    assert captured_pipeline_run.pipeline_name == PipelineType.PDF_SUMMARIZER
    assert captured_pipeline_run.status == PipelineRunStatus.QUEUED
    assert captured_pipeline_run.uploaded_file_log_id == 1
    assert captured_pipeline_run.celery_task_id == mock_celery_task_result.id
    assert isinstance(captured_pipeline_run.run_uuid, uuid.UUID)

    mock_delay.assert_called_once_with(
        str(captured_pipeline_run.run_uuid),
        mock_uploaded_file_log.id,
        mock_uploaded_file_log.storage_location,
        mock_uploaded_file_log.filename
    )

    assert isinstance(response, PipelineRunRead)
    assert response.run_uuid == captured_pipeline_run.run_uuid
    assert response.celery_task_id == mock_celery_task_result.id
    assert response.status == PipelineRunStatus.QUEUED

